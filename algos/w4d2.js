/**
 * Efficiently determines if the given num exists in the given array.
 * - Time: O(?).
 * - Space: O(?).
 * @param {Array<number>} sortedNums
 * @param {number} searchNum
 * @returns {boolean} Whether the given num exists in the given array.
 */
function binarySearch(sortedNums, searchNum, start, end, called = 0) { 
    if (start > end) {
        return called;
    }
    var mid = Math.floor((start + end)/2);
    if (sortedNums[mid] == searchNum) {
        called++;
        return binarySearch(sortedNums, searchNum, start + 1, end, called);
        
    }
    if (sortedNums[mid] > searchNum) {
        return binarySearch(sortedNums, searchNum, start, mid-1, called);
    } else {
        return binarySearch(sortedNums, searchNum, mid + 1, end, called);
    }
}
